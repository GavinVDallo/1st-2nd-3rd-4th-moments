# Function to calculate central moments
calc_central_moments <- function(x) {
  n <- length(x)
  mu <- mean(x)
  
  m1 <- sum((x - mu)^1) / n
  m2 <- sum((x - mu)^2) / n
  m3 <- sum((x - mu)^3) / n
  m4 <- sum((x - mu)^4) / n
  
  return(c(M1=round(m1), M2=m2, M3=m3, M4=m4))
}

